export function useT(lang) {
  return (path, vars = {}) => {
    const parts = path.split('.');
    let val = T[lang] ?? T.en;
    for (const p of parts) val = val?.[p];
    if (!val) return path;
    return Object.entries(vars).reduce(
      (s, [k, v]) => s.replace(`{${k}}`, v),
      val
    );
  };
}
